    private static String getThreadMessages(java.lang.String threadId) {
        ChatService.ChatService_Service service = new ChatService.ChatService_Service();
        ChatService.ChatService port = service.getChatServicePort();
        return port.getThreadMessages(threadId);
    }

    private static void addMessage(java.lang.String nickName, java.lang.String message, java.lang.String threadId, java.lang.String loginId) {
        ChatService.ChatService_Service service = new ChatService.ChatService_Service();
        ChatService.ChatService port = service.getChatServicePort();
        port.addMessage(nickName, message, threadId, loginId);
    }

    private static String checkNewThreadMessages(java.lang.String threadId, int latestMessageId) {
        ChatService.ChatService_Service service = new ChatService.ChatService_Service();
        ChatService.ChatService port = service.getChatServicePort();
        return port.checkNewThreadMessages(threadId, latestMessageId);
    }

