    private void showThreadMessages() {

        String checkThreadMessage = checkNewThreadMessages(threadId, latestMessageId);

        String infoArray[] = checkThreadMessage.split("-");

        latestMessageId = Integer.parseInt(infoArray[1]);

        boolean is_a_new_message_added = Boolean.valueOf(infoArray[0]);

        if (is_a_new_message_added || did_the_window_open_first) {
            String receivedThreadChain = getThreadMessages(threadId);

            String receivedThreadChainArr[] = receivedThreadChain.split(";");

            DefaultTableModel model = (DefaultTableModel) messageTable.getModel();

            model.setRowCount(0);

            Object[] rows = new Object[3];

            int columnNo = 0;

            for (int i = 0; i < receivedThreadChainArr.length; i++) {
                String threadRowDataArr[] = receivedThreadChainArr[i].split(">");
                for (int j = 0; j < threadRowDataArr.length; j++) {
                    rows[columnNo] = threadRowDataArr[j];
                    columnNo++;
                }
                columnNo = 0;
                model.addRow(rows);
            }
            did_the_window_open_first = false;
            scrolltable();
        } else {

        }

    }

    public void scrolltable() {
        messageTable.addComponentListener(new ComponentAdapter() {
            public void componentResized(ComponentEvent e) {
                messageTable.scrollRectToVisible(messageTable.getCellRect(messageTable.getRowCount() - 1, messageTable.getColumnCount(), true));
            }
        });
    }

